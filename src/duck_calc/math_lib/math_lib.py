"""
    example for math lib
"""


class ExampleMathLib():
    """ Some epic docstring
    """

    @staticmethod
    def sum_a_b(num_a, num_b):
        """Sum A and B

            args:
                a (int)
                b (int)

            returns:
                int
        """
        return num_a + num_b

    @staticmethod
    def odd_a_b(num_a, num_b):
        """Odd A and B

            args:
                a (int)
                b (int)

            returns:
                int
        """
        return num_a - num_b
